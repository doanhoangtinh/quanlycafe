    private void btnSuaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMousePressed
        // TODO add your handling code here:
        ChucVu chucVu = null;
        NhanVien nhanVien = new NhanVien();
        for (int i = 0; i < chucVuList.size(); i++) {
            if (cboCV.getSelectedItem().toString().equals(chucVuList.get(i).getTenChucVu())) {
                chucVu = new ChucVu(chucVuList.get(i).getMaChucVu(), chucVuList.get(i).getTenChucVu());
            }
        }
        nhanVien.setMaNhanVien(txtMaNV.getText());
        nhanVien.setTenNhanVien(txtTenNV.getText());
        nhanVien.setTaiKhoan(txtTaiKhoan.getText());
        nhanVien.setMatKhau(txtMatKhau.getText());
        nhanVien.setChucVu(chucVu);
        nhanVien.setDiaChi(txtDiaChi.getText());
        nhanVien.setCmnd(txtCmnd.getText());
        nhanVien.setSdt(txtSdt.getText());
        int res = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn tiếp tục?", "Xác nhận ", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            dBConnection.editNhanVien(nhanVien, maNhanVien);
            QuanLyNhanVien.loadNhanVien();
        }
    }//GEN-LAST:event_btnSuaMousePressed

