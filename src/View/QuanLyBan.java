    public static void loadBan() {
        String[] title = {"Mã Bàn", "Số Lượng Chỗ", "Vị Trí", "Trạng Thái"};
        DefaultTableModel defaultTableModel = new DefaultTableModel(title, 0);
        DBConnection dBConnection = new DBConnection();
        ArrayList<Ban> banList = dBConnection.getDSBan();
        for (int i = 0; i < banList.size(); i++) {
            Vector vector = new Vector();
            vector.add(banList.get(i).getMaBan());
            vector.add(banList.get(i).getSoLuongCho());
            vector.add(banList.get(i).getViTri());
            vector.add(banList.get(i).getTrangThai());
            defaultTableModel.addRow(vector);
        }        
        tableQuanLyBan.setRowHeight(35);
        tableQuanLyBan.setModel(defaultTableModel);
    }

