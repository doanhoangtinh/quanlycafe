    private void btnXoaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMousePressed
        // TODO add your handling code here:
        String maban = tableQuanLyBan.getValueAt(tableQuanLyBan.getSelectedRow(), 0).toString();
        int soluong = Integer.parseInt(tableQuanLyBan.getValueAt(tableQuanLyBan.getSelectedRow(), 1).toString());
        String vitri = tableQuanLyBan.getValueAt(tableQuanLyBan.getSelectedRow(), 2).toString();
        String trangthai = tableQuanLyBan.getValueAt(tableQuanLyBan.getSelectedRow(), 3).toString();
        Ban b = new Ban(maban,soluong,vitri,trangthai);
        DBConnection dBConnection = new DBConnection();
        int res = JOptionPane.showConfirmDialog(this, "Bạn có muốn tiếp tục?", "Xác nhận ", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            dBConnection.deleteBan(b);
            loadBan();
        }
    }//GEN-LAST:event_btnXoaMousePressed


